def parse_ascii(text):
    lines = text.strip('\n').rstrip().split('\n')
    rows = []
    for ln in lines:
        ln = ln.rstrip()
        row = [1 if i < len(ln) and ln[i] != ' ' else 0 for i in range(W)]
        rows.append(row)
    while len(rows) < H:
        rows.append([0]*W)
    return rows[:H]

def to_ssd1306(rows):
    buf = []
    for p in range(PAGES):
        for c in range(W):
            byte = 0
            for b in range(8):
                y = p * 8 + b
                if y < H and rows[y][c]:
                    byte |= (1 << b)
            buf.append(byte)
    return buf

# Generate output
lines = [
    '/**',
    ' * @file    pet_sprites.c',
    ' * @brief   小狗48x48点阵 (自动生成)',
    ' */',
    '#include "pet_sprites.h"',
    ''
]

for name, frames in sprites.items():
    for fi, frame in enumerate(frames):
        buf = to_ssd1306(parse_ascii(frame))
        lines.append(f'const uint8_t {name}_f{fi}[SPRITE_SIZE] = {{')
        for i in range(0, len(buf), 12):
            chunk = ', '.join(f'0x{b:02X}' for b in buf[i:i+12])
            lines.append(f'\t{chunk},')
        lines.append('};')
        lines.append('')

