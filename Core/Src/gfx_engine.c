void GFX_Clear(void)     { SSD1306_ClearBuffer(); }
void GFX_Refresh(void)   { SSD1306_Refresh(); }
void GFX_SetPixel(uint8_t x, uint8_t y) { SSD1306_SetPixel(x, y, 1); }
void GFX_ClrPixel(uint8_t x, uint8_t y) { SSD1306_SetPixel(x, y, 0); }

void GFX_DrawLine(uint8_t x0, uint8_t y0, uint8_t x1, uint8_t y1)
{
    int dx = (x1 > x0) ? (x1 - x0) : (x0 - x1);
    int dy = (y1 > y0) ? (y1 - y0) : (y0 - y1);
    int sx = (x0 < x1) ? 1 : -1;
    int sy = (y0 < y1) ? 1 : -1;
    int err = dx - dy;

    while (1) {
        GFX_SetPixel(x0, y0);
        if (x0 == x1 && y0 == y1) break;
        int e2 = 2 * err;
        if (e2 > -dy) { err -= dy; x0 += sx; }
        if (e2 <  dx) { err += dx; y0 += sy; }
    }
}

void GFX_DrawRect(uint8_t x, uint8_t y, uint8_t w, uint8_t h)
{
    GFX_DrawLine(x, y, x+w-1, y);
    GFX_DrawLine(x, y+h-1, x+w-1, y+h-1);
    GFX_DrawLine(x, y, x, y+h-1);
    GFX_DrawLine(x+w-1, y, x+w-1, y+h-1);
}

void GFX_FillRect(uint8_t x, uint8_t y, uint8_t w, uint8_t h)
{
    for (uint8_t yy = y; yy < y + h && yy < 64; yy++)
        for (uint8_t xx = x; xx < x + w && xx < 128; xx++)
            GFX_SetPixel(xx, yy);
}

void GFX_DrawBitmap(uint8_t x, uint8_t y, uint8_t w, uint8_t h,
                    const uint8_t *bitmap)
{
    uint8_t pages = (h + 7) / 8;  /* 向上取整到字节 */
    for (uint8_t py = 0; py < pages; py++) {
        for (uint8_t px = 0; px < w; px++) {
            uint8_t byte = bitmap[py * w + px];
            for (uint8_t bit = 0; bit < 8; bit++) {
                uint8_t yy = y + py * 8 + bit;
                if (yy >= 64) break;
                if (byte & (1 << bit))
                    GFX_SetPixel(x + px, yy);
            }
        }
    }
}

void GFX_DrawChar(uint8_t x, uint8_t y, char c)
{
    if (c < 32 || c > 126) c = '?';
    const uint8_t *glyph = font5x7[c - 32];

    for (uint8_t col = 0; col < 5; col++) {
        uint8_t byte = glyph[col];
        for (uint8_t row = 0; row < 7; row++) {
            if (byte & (1 << row))
                GFX_SetPixel(x + col, y + row);
        }
    }
}

void GFX_DrawString(uint8_t x, uint8_t y, const char *str)
{
    uint8_t cx = x, cy = y;
    while (*str) {
        if (*str == '\n' || cx > 128 - 6) {
            cx = x;
            cy += 9;
            if (cy > 64 - 8) break;
        }
        if (*str != '\n') {
            GFX_DrawChar(cx, cy, *str);
            cx += 6;
        }
        str++;
    }
}
